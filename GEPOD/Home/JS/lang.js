    // Function to apply the selected language
    function applyLanguage(lang) 
    {
        for (let id in texts[lang]) 
        {
            let el = document.getElementById(id);
            if (el) 
            {
                const value = texts[lang][id];
                
                if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
                    el.placeholder = value;
                } 
                else if (value.includes("<br>")) {
                    el.innerHTML = value;
                } else {
                    el.textContent = value;
                }
            }
        }

        // Change page direction for Arabic (RTL)
        document.documentElement.dir = (lang === "ar") ? "rtl" : "ltr";
        
        // Set the selected option in the language dropdown
        const langSelect = document.getElementById('language-select');
        if (langSelect) 
        {
            langSelect.value = lang;
         }
    }

    // Function to change language and save preference
    function changeLanguage(lang) 
    {
        localStorage.setItem('selectedLanguage', lang);
        
        applyLanguage(lang);
    }

    // When the page loads
    document.addEventListener('DOMContentLoaded', function() 
    {
        const savedLang = localStorage.getItem('selectedLanguage') || 'fr';
        
        applyLanguage(savedLang);
        
        const langSelect = document.getElementById('language-select');
        if (langSelect) 
        {
            langSelect.addEventListener('change', function() 
            {
                changeLanguage(this.value);
            });
        }
    });
